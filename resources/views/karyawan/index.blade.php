    <script>
        $(function() {
            $("#btnTambahKaryawan").click(function() {
                $("#modal-tambahKaryawan").modal("show");
            });

            $(".edit").click(function() {
                let nip = $(this).attr('nip');

                $.ajax({
                    type: 'POST',
                    url: '{{ route('karyawan.edit') }}',
                    cache: false,
                    data: {
                        _token: "{{ csrf_token() }}",
                        nip: nip
                    },
                    success: function(response) {
                        $("#loadEditForm").html(response);
                    },
                    error: function(xhr, status, error) {
                        console.error("AJAX error:", xhr.responseText);
                    }
                });
                $("#modal-editKaryawan").modal("show");
            });

            $(".delete-confirm").click(function(e) {
                let form = $(this).closest('form');

                e.preventDefault();

                Swal.fire({
                    title: "Apakah Anda Yakin Ingin Menghapus Data Karyawan Ini?",
                    text: "Data Akan Dihapus Secara Permanen",
                    icon: "warning",
                    showCancelButton: true,
                    confirmButtonColor: "#3085d6",
                    cancelButtonColor: "#d33",
                    confirmButtonText: "Ya, Hapus"
                }).then((result) => {
                    if (result.isConfirmed) {
                        form.submit();
                        Swal.fire({
                            title: "Deleted!",
                            text: "Data Karyawan Berhasil Dihapus.",
                            icon: "success"
                        });
                    }
                });
            });

            $("#frmKaryawan").submit(function() {
                let nip = $("#nip").val();
                let nama_lengkap = $("#nama_lengkap").val();
                let pendidikan = $("#pendidikan").val();
                let jabatan = $("#jabatan").val();
                let nomor_hp = $("#nomor_hp").val();

                if (nip === "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'NIP Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#nip").focus();
                    })

                    return false;
                } else if (nama_lengkap === "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Nama Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#nama_lengkap").focus();
                    })

                    return false;
                } else if (pendidikan === "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Nama Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#pendidikan").focus();
                    })

                    return false;
                } else if (jabatan === "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Nama Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#jabatan").focus();
                    })

                    return false;
                } else if (nomor_hp === "") {
                    Swal.fire({
                        title: 'Warning!',
                        text: 'Nama Harus Diisi',
                        icon: 'warning',
                        confirmButtonText: 'Ok'
                    }).then((result) => {
                        $("#nomor_hp").focus();
                    })

                    return false;
                }
            })
        });
    </script>
